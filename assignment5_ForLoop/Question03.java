package assignment5_ForLoop;

public class Question03 {

	public static void main(String[] args) {

	//Write a program that displays the number of even numbers between 5 and 50 (included)

		int n=50;  
		 
		for (int i=5; i<=n; i++){  
					
			if (i%2==0){  
		
				System.out.print(i + " ");  
			}  
		}  
		
	}

}
